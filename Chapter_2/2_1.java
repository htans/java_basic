class Ex2_1 {
    public static void main(String[] args) {
        // 자바는 대소문자를 구분한다. System을 system으로 입력하지 않도록 주의
        System.out.println("Hello,world");  // 화면에 Hello, world을 출력하고 줄바꿈을 한다.

        System.out.print("Hello");          // 화면에 Hello를 출력하고 줄바꿈을 안한다.
        System.out.println("World");        // 화면에 World를 출력하고 줄바꿈을 한다.
    }
}